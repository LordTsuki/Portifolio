package aulas.exer;

public class Aluno 
{
    private float alt, peso;
    private String nome;

    public void setNome(String nome)
    {
        this.nome = nome;
    }

    public String getNome()
    {
        return nome;
    }

    public void setAlt(float alt)
    {
        this.alt = alt;
    }

    public float getAlt()
    {
        return alt;
    }

    public void setPeso(float peso)
    {
        this.peso = peso;
    }

    public float getPeso()
    {
        return peso;
    }
}
